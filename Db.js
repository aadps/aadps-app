var SQLite = require('react-native-sqlite-storage');
SQLite.enablePromise(true);

// Options Table
//
// 1: Ver
// 2: User
// 3: Fav
// 4: PushChan
// 5: PushView

class Db {
  constructor() {
    SQLite.openDatabase('aadps.db', '1.0', 'AADPS App Database', 200000).then((DB) => {
      this._db = DB;
      this._db.executeSql('SELECT * FROM options', [], () => {}, () => {this.initialize();});
    });
  }

  initialize() {
    this._db.executeSql('CREATE TABLE IF NOT EXISTS options (id INT, data TEXT, PRIMARY KEY(id))').then(() => {});
    this._db.executeSql('CREATE TABLE IF NOT EXISTS colleges (id INT,' +
      'type VARCHAR, geo INT, comp INT, size INT, name VARCHAR, cname VARCHAR,' +
      'type2 VARCHAR, city VARCHAR, setting VARCHAR, ceeb VARCHAR, ranking INT,' +
      'comment VARCHAR, order1 INT, order2 INT, order3 INT, guide INT, PRIMARY KEY(id))');
    this._db.executeSql('CREATE INDEX IF NOT EXISTS collidx on colleges (geo, comp, size)');
    this._db.executeSql('CREATE TABLE IF NOT EXISTS channels (id INT,' +
      'name VARCHAR, thumb VARCHAR, msg VARCHAR, date VARCHAR, isNew INT, PRIMARY KEY(id))');
    for(var i = 1; i < 6; i++)this._db.executeSql('INSERT INTO options VALUES (?, "")', [i]);
    this.loadColleges();
  }

  loadColleges() {
    this._db.executeSql('SELECT data FROM options where id = 1')
    .then(([result]) => {
      if(result.rows.item(0).data != ver){
        this._db.executeSql('UPDATE options SET data = "' + ver + '" WHERE id = 1');
        this._db.executeSql('DROP TABLE colleges');
        this._db.executeSql('CREATE TABLE IF NOT EXISTS colleges (id INT,' +
          'type VARCHAR, geo INT, comp INT, size INT, name VARCHAR, cname VARCHAR,' +
          'type2 VARCHAR, city VARCHAR, setting VARCHAR, ceeb VARCHAR, ranking INT,' +
          'comment VARCHAR, order1 INT, order2 INT, order3 INT, guide INT, PRIMARY KEY(id))');
        for(var i in data)this._db.executeSql('INSERT INTO colleges VALUES ' +
        '(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)',
        [data[i].id, data[i].type, data[i].geo, data[i].comp, data[i].size,
        data[i].name, data[i].cname, data[i].type2, data[i].city, data[i].setting,
        data[i].ceeb, data[i].ranking, data[i].comment, data[i].order1, data[i].order2,
        data[i].order3, data[i].guide]);
      }
    })
    .catch(function(e) {});
  }

  setUser(user, passwd, profile) {
    return this._db.executeSql("UPDATE options SET data = '" +
    JSON.stringify({user: user, passwd: passwd, profile: profile})
    + "' WHERE id = 2");
  }

  getUser() { // myDb.getUser().then(result => { ... });
    return this._db.executeSql('SELECT data FROM options WHERE id = 2')
    .then(([result])=> {
      if(result.rows.item(0).data)return JSON.parse(result.rows.item(0).data);
      else return null;
    })
    .catch(function(e) {});
  }

  erase() {
    this._db.executeSql('DELETE FROM channels');
    return this._db.executeSql('UPDATE options SET data = "" WHERE id = 2');
  }

  setFav(fav, time) {
    return this._db.executeSql("UPDATE options SET data = '" +
    JSON.stringify({fav: fav, time: time})
    + "' WHERE id = 3");
  }

  getFav() {
    return this._db.executeSql('SELECT data FROM options WHERE id = 3')
    .then(([result])=> {
      if(result.rows.item(0).data)return JSON.parse(result.rows.item(0).data);
      else return null;
    })
    .catch(function(e) {});
  }

  getCardData(fav, order) {
    var orderby = '';
    switch(order){
      case 0: orderby = 'ORDER BY order1 asc'; break;
      case 1: orderby = 'ORDER BY order2 asc'; break;
      case 2: orderby = 'ORDER BY order3 asc'; break;
      default: break;
    }
    return this._db.executeSql('SELECT * FROM colleges WHERE id IN (' +
    fav.join(',') + ') ' + orderby)
    .then(([result])=> {
      if(result.rows.item(0)){
        var colleges = [];
        for(var i = 0; i < result.rows.length; i++)colleges.push(result.rows.item(i));
        return colleges;
      }
      else return null;
    });
  }

  filter(data) { // myDb.filter([6, 11, 21]).then(result => { ... });
    var geo = [], comp = [], size = [];

    for(var i in data){
      if(parseInt(data[i] / 10) == 0)geo.push(data[i] % 10);
      else if(parseInt(data[i] / 10) == 1)comp.push(data[i] % 10);
      else if(parseInt(data[i] / 10) == 2)size.push(data[i] % 10);
    }

    return this._db.executeSql('SELECT id, cname FROM colleges WHERE geo IN (' +
  geo.join(',') + ') AND comp IN (' + comp.join(',') + ') AND size IN (' +
  size.join(',') + ')')
    .then(([result])=> {
      if(result.rows.item(0)){
        var colleges = [];
        for(var i = 0; i < result.rows.length; i++)colleges.push(result.rows.item(i));
        return colleges;
      }
      else return null;
    });
  }

  getPushChan() {
    return this._db.executeSql('SELECT data FROM options WHERE id = 4')
    .then(([result])=> {
      if(result.rows.item(0).data)return result.rows.item(0).data;
      else return null;
    })
    .catch(function(e) {});
  }

  getPushView() {
    return this._db.executeSql('SELECT data FROM options WHERE id = 5')
    .then(([result])=> {
      if(result.rows.item(0).data){
        this._db.executeSql('UPDATE options SET data = "" WHERE id = 5')
        return parseInt(result.rows.item(0).data);
      }
      else return null;
    })
    .catch(function(e) {});
  }

  async getChan(newChan) {
    if(newChan){
      let result = await this._db.executeSql('SELECT * FROM channels')

      for(var i = 0; i < result[0].rows.length; i++){
        for(var j = 0; j < newChan.length; j++){
          if(result[0].rows.item(i).id == newChan[j].id){
            newChan[j].flag = true;
            if(result[0].rows.item(i).date < newChan[j].date)
            await this._db.executeSql('UPDATE channels SET isNew = '+ newChan[j].isNew +', msg="' + newChan[j].msg + '", date="'
            + newChan[j].date + '" WHERE id =' + newChan[j].id);
          }
        }
      }

      for(var i =0; i < newChan.length; i++)if(!newChan[i].flag)await this._db.executeSql('INSERT INTO channels VALUES ("' + newChan[i].id + '", "'
      + newChan[i].name + '", "' + newChan[i].thumb + '", "' + newChan[i].msg + '", "'
      + newChan[i].date + '", ' + newChan[i].isNew + ')');
    }
    return this._db.executeSql('SELECT * FROM channels ORDER BY date DESC')
    .then(([result])=> {
      if(result.rows.item(0)){
        var channels = [];
        for(var i = 0; i < result.rows.length; i++)channels.push(result.rows.item(i));
        return channels;
      }
      else return null;
    });
  }

  setChanOld(id) {
    this._db.executeSql('UPDATE channels SET isNew = 0 WHERE id =' + id);
  }
}

